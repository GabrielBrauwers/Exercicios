    private void TabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaMouseClicked
        if (evt.getClickCount() == 2) {
        
        int linha, id;

        linha = Tabela.getSelectedRow();

        if (linha != -1) {
            
            try {
                Object idObjeto = Tabela.getModel().getValueAt(linha, 0); 
                
                if (idObjeto instanceof Integer integer) {
                    id = integer;
                } else {
                    id = Integer.parseInt(idObjeto.toString()); 
                }

                JFrame novo = new AlterarUsuario(id);
                novo.setLocationRelativeTo(rootPane);
                novo.setVisible(true);
                dispose();

            } catch (NumberFormatException e) {
                System.err.println("Erro de formato de número na JTable: " + e.getMessage());
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "O ID da linha selecionada não é um número válido.", 
                    "Erro de Dados", javax.swing.JOptionPane.ERROR_MESSAGE);
            } catch (ErpDAOException ex) {
                Logger.getLogger(GerenciamentoDeUsuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }     
    }//GEN-LAST:event_TabelaMouseClicked

