     public void carrega() {

        DefaultTableModel table = (DefaultTableModel) Tabela.getModel();
        String nome,login,adm,inat;
        int id,admin,inativo;
        DateFormat dtOutput = new SimpleDateFormat("dd/MM/yyyy");

        if (Tabela.getRowCount() > 0) {
            while (Tabela.getRowCount() > 0) {
                table.removeRow(0);
            }
        }

        UsuarioDAO CDAO;

        try {
            CDAO = new UsuarioDAO();
            usuarios = CDAO.Listar();
        } catch (Exception ex) {
            System.out.println("Erro de conexão ao banco.");
        }

        for (int i = 0; i < usuarios.size(); i++) {
            id = usuarios.get(i).getID_usuario();
            nome = usuarios.get(i).getNome();
            login = usuarios.get(i).getLogin();
            admin = usuarios.get(i).getAdministrador();
            inativo = usuarios.get(i).getInativo();
            if(admin == 0){
                adm = "Nao";
            } else{
                adm = "Sim";
            }
            if(inativo == 0){
                inat = "Ativo";
            }else{
                inat = "Inativo";
            }
            table.addRow(new String[]{String.valueOf(id),nome,login,adm,inat});
        }
    }
